    public void PasarDatosT() {
        int pos = tablePelicula.getSelectedRow();

        txtPeliculaId.setText(tablePelicula.getValueAt(pos, 0).toString());
        txtTitulo.setText(tablePelicula.getValueAt(pos, 1).toString());
        txtCategoria.setText(tablePelicula.getValueAt(pos, 2).toString());
        try {
            Date fecha = frmt.parse(tablePelicula.getValueAt(pos, 3).toString());
            txtFecha.setDate(fecha);
        } catch (ParseException ex) {
            Logger.getLogger(VendedorPelicula.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtDuracion.setText(tablePelicula.getValueAt(pos, 4).toString());
        txtIdioma.setText(tablePelicula.getValueAt(pos, 5).toString());
        Botones(false, true, true, true, false);
        Escribir(false);
    }

    public void Recarga() {
        CargarDatosT();
        Limpiar();
        Escribir(false);
        Botones(false, true, false, false, false);
    }

