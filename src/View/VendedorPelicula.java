    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (Validar()) {

            String Mensaje = "";
            int id = Integer.parseInt(txtPeliculaId.getText());
            String Titulo = txtTitulo.getText();
            String Categoria = txtCategoria.getText();
            Date Fecha = txtFecha.getDate();
            String Duracion = txtDuracion.getText();
            String Idioma = txtIdioma.getText();
            Pelicula pelicula = new Pelicula(id, Titulo, Categoria, Fecha, Duracion, Idioma);
            if (Opc == 1) {
                peliculaController.insert(pelicula);
                Mensaje = "Nueva pelicula registrada exitosamente";
            } else {
                peliculaController.update(pelicula);
                Mensaje = "La pelicula ha sido actualziada";
            }
            MsgInformation(Mensaje);
            Recarga();

        } else {
            MsgError("Se encontraron datos vacios o invalidos");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

