    public void CargarDatosT() {
        for (int i = 0; i < tableCliente.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
        for (Cliente clienteT : clienteController.findAll()) {
            modelo.addRow(new Object[]{
                clienteT.getCodigo(),
                clienteT.getNombres(),
                clienteT.getApellidos(),
                clienteT.getDni(),
                clienteT.getCorreo(),
                clienteT.getCelular(),
                clienteT.getContrase単a(),
                clienteT.getTarjeta(),});
        }
        Color fondo = new Color(102, 102, 102);
        scroll.getViewport().setBackground(fondo);
    }

