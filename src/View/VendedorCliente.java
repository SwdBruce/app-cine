    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (Validar()) {
            String Mensaje = "";
            int id = Integer.parseInt(txtUsuarioId.getText());
            String Nombres = txtNombres.getText();
            String Apellidos = txtApellidos.getText();
            int DNI = Integer.parseInt(txtDNI.getText());
            String Correo = txtCorreo.getText();
            int Celular = Integer.parseInt(txtCelular.getText());
            String Contrase単a = txtContrase単a.getText();
            String Tarjeta = txtTarjeta.getText();
            Cliente cliente = new Cliente(id, Nombres, Apellidos, DNI, Correo, Celular, Contrase単a, 2, Tarjeta);
            if (Opc == 1) {
                clienteController.insert(cliente);
                Mensaje = "Nuevo cliente registrado exitosamente";
            } else {
                clienteController.update(cliente);
                Mensaje = "El cliente ha sido actualziado";
            }
            MsgInformation(Mensaje);
            Recarga();
        } else {
            MsgError("Se encontraron datos vacios o invalidos");
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

